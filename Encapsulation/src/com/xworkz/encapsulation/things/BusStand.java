package com.xworkz.encapsulation.things;

public class BusStand {
	private String name;
	private String city;
	private int routes;
	private int busses;
	private double area;
	private int depots;
	private String nearCity;
	private String mananger;
	private int staffes;
	private int chairs;
	private boolean claen;

	public String getName() {
		return name;
	}

	void setName(String name) {
		this.name = name;
	}

	public String getCity() {
		return city;
	}

	void setCity(String city) {
		this.city = city;
	}

	public int getRoutes() {
		return routes;
	}

	void setRoutes(int routes) {
		this.routes = routes;
	}

	public int getBusses() {
		return busses;
	}

	void setBusses(int busses) {
		this.busses = busses;
	}

	public double getArea() {
		return area;
	}

	void setArea(double area) {
		this.area = area;
	}

	public int getDepots() {
		return depots;
	}

	void setDepots(int depots) {
		this.depots = depots;
	}

	public String getNearCity() {
		return nearCity;
	}

	void setNearCity(String nearCity) {
		this.nearCity = nearCity;
	}

	public String getMananger() {
		return mananger;
	}

	void setMananger(String mananger) {
		this.mananger = mananger;
	}

	public int getStaffes() {
		return staffes;
	}

	void setStaffes(int staffes) {
		this.staffes = staffes;
	}

	public int getChairs() {
		return chairs;
	}

	void setChairs(int chairs) {
		this.chairs = chairs;
	}

	public boolean isClaen() {
		return claen;
	}

	void setClaen(boolean claen) {
		this.claen = claen;
	}

}
