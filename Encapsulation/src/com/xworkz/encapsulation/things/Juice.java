package com.xworkz.encapsulation.things;

public class Juice {
	private String name;
	private String conten;
	private int rate;
	private int quantity;
	private int glass;
	private int types;
	private String ice;
	private String sugur;
	private String milk;
	private String mixer;
	private String total;
	private boolean liked;
	private boolean teaste;

	public String getName() {
		return name;
	}

	void setName(String name) {
		this.name = name;
	}

	public String getConten() {
		return conten;
	}

	void setConten(String conten) {
		this.conten = conten;
	}

	public int getRate() {
		return rate;
	}

	void setRate(int rate) {
		this.rate = rate;
	}

	public int getQuantity() {
		return quantity;
	}

	void setQuantity(int quantity) {
		this.quantity = quantity;
	}

	public int getGlass() {
		return glass;
	}

	void setGlass(int glass) {
		this.glass = glass;
	}

	public int getTypes() {
		return types;
	}

	void setTypes(int types) {
		this.types = types;
	}

	public String getIce() {
		return ice;
	}

	void setIce(String ice) {
		this.ice = ice;
	}

	public String getSugur() {
		return sugur;
	}

	void setSugur(String sugur) {
		this.sugur = sugur;
	}

	public String getMilk() {
		return milk;
	}

	void setMilk(String milk) {
		this.milk = milk;
	}

	public String getMixer() {
		return mixer;
	}

	void setMixer(String mixer) {
		this.mixer = mixer;
	}

	public String getTotal() {
		return total;
	}

	void setTotal(String total) {
		this.total = total;
	}

	public boolean isLiked() {
		return liked;
	}

	void setLiked(boolean liked) {
		this.liked = liked;
	}

	public boolean isTeaste() {
		return teaste;
	}

	void setTeaste(boolean teaste) {
		this.teaste = teaste;
	}

}
